var ComplexType = map[byte]bool{
	FIX_ARRAY_START: true,
	DYN_ARRAY_START: true,
	STRUCT_START:    true,
	STRING:          true,
}

// signed int
func IsSint(code byte) bool {
	return (INT <= code && code <= INT256)
}

func IsUint(code byte) bool {
	return (UINT <= code && code <= UINT256) || (BYTE1 <= code && code <= BYTE32)
}

func IsBool(code byte) bool {
	return BOOL == code
}
